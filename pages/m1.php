	<h1>Objek 3 Dimensi</h1>
	<img src="img/3d.jpg" alt="3d" style="height:228px;"><br>
3 Dimensi biasa disebut 3D adalah bentuk dari benda yang memiliki panjang, lebar, dan tinggi. Grafik 3 Dimensi merupakan teknik penggambaran yg berpatokan pada titik koordinat sumbu x (datar), sumbu y (tegak), dan sumbu z (miring). Representasi dari data geometrik 3 dimensi sebagai hasil dari pemrosesan dan pemberian efek cahaya terhadap grafika komputer 2D. Tiga Dimensi, biasanya digunakan dalam penanganan grafis. 3D secara umum merujuk pada kemampuan dari sebuah video card.
<br>
Grafik 3D merupakan perkembangan dari grafik 2D. Didalam grafika komputer, 3D merupakan bentuk grafik yang menggunakan representasi data geometri tiga dimensi. Suatu objek rangka 3D apabila disinari dari arah tertentu akan membentuk bayangan pada permukaan gambar. 
<br>
Proses pembuatan grafik komputer 3D dapat dibagi ke dalam tiga fase, yaitu 3D modeling yang mendeskripsikan bentuk dari sebuah objek, layout dan animation yang mendeskripsikan gerakan dan tata letak sebuah objek, dan 3D rendering yang memproduksi image dari objek tersebut.
<br>
<h3>Karakteristik objek 3D</h3>
karakteristik  tiga dimensi  adalah sebagai berikut : <br>
· Pesan yang sama dapat disebarkan keseluruh siswa secara serentak.<br>
· Penyajiannya berada dalam kontrol guru.<br>
· Cara penyimpanannya mudah (praktis).<br>
· Dapat mengatasi keterbatasan ruang, waktu, dan indera.<br>
· Menyajikan objek-objek secara diam.<br>
· Terkadang dalam penyajiannya memerlukan ruangan gelap.<br>
· Lebih mahal dari kelompok media grafis.<br>
· Sesuai untuk mengajarkan keterampilan tertentu.<br>
· Sesuai untuk belajar secara berkelompok atau individual.<br>
· Praktis digunakan untuk semua ukuran ruangan kelas.<br>
· Mampu menyajikan teori dan praktik secara terpadu.<br>
<h3>Fertex, Edge, Face</h3>
- Vertex adalah titik-titik yang dihubungkan dengan edge.<br>
- Edge adalah garis yang menghubungkan lebih dari dua vertex (titik).<br>
- Face adalah sebuah permukaan yang dibentuk oleh minimal 3 edges yang tertutup.<br>
